/*!
 *  @brief      显示6*8字符
 *  @author     Nan Li
 *  @param      site    左上角坐标
 *  @param      ascii   字符
 *  @param      Color   字体颜色
 *  @param      bkColor 背景颜色
 *  @since      v1.0
*  Sample usage:        Site_t site = {10,20};   //x = 10 ,y = 20
                        LCD_P6x8Char(site,'0', BLUE,RED);
 */
void LCD_P6x8Char(Site_t site, u8 ascii, u16 Color, u16 bkColor)
{
#define MAX_CHAR_POSX (LCD_W-6)
#define MAX_CHAR_POSY (LCD_H-8)

    u8 temp,c,t;
    Size_t size = {6, 8};

    if(site.x > MAX_CHAR_POSX || site.y > MAX_CHAR_POSY)
    {
        return;
    }

    LCD_PTLON(site, size);

    LCD_RAMWR();                    //写内存
    
    c =ascii-32;
    
    for(t=0;t<8;t++)     
    {
      unsigned char i=0;
	
      while(i++<6)
      {
        temp=(ascii_6x8[c][i]>>t)&0x01;
        if(temp)
        {
          LCD_WR_DATA(Color);
        }
        else
        {
          LCD_WR_DATA(bkColor);
        }
      }
    }
    
    //return;
#undef MAX_CHAR_POSX
#undef MAX_CHAR_POSY
}

/*!
 *  @brief      显示8*16字符
 *  @param      site    左上角坐标
 *  @param      ascii   字符
 *  @param      Color   字体颜色
 *  @param      bkColor 背景颜色
 *  @since      v1.0
*  Sample usage:        Site_t site = {10,20};   //x = 10 ,y = 20
                        LCD_P6x8Char(site,'0', BLUE,RED);
 */
void LCD_P8x16Char(Site_t site, u8 ascii, u16 Color, u16 bkColor)
{
#define MAX_CHAR_POSX (LCD_W-8)
#define MAX_CHAR_POSY (LCD_H-16)

    uint8 temp, t, pos;
    Size_t size = {8, 16};

    if(site.x > MAX_CHAR_POSX || site.y > MAX_CHAR_POSY)
    {
        return;
    }

    LCD_PTLON(site, size);

    LCD_RAMWR();                    //写内存

    for (pos = 0; pos < 16; pos++)
    {
        temp = ascii_8x16[((ascii-0x20)*16)+pos];

        for(t = 0; t < 8; t++)
        {

            if(temp & 0x80)
            {
                LCD_WR_DATA(Color);
            }
            else
            {
                LCD_WR_DATA(bkColor);
            }
            temp <<= 1;
        }
    }
    //return;
#undef MAX_CHAR_POSX
#undef MAX_CHAR_POSY
}
